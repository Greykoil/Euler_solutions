long long pjeProblem_8::run()
{
	std::vector<long long> nums_in_vec;
	get_ints_in_vec(nums_in_vec);
	long long highest_total = 0;

	for (int i = 0, size = nums_in_vec.size(); i < size - 13; ++i) {
		long long current_total = 1;
		for (int j = 0; j < 13; ++j) {
			current_total *= (nums_in_vec[i + j]);
		}
		highest_total = std::max(highest_total, current_total);
	}
	return highest_total;
}

